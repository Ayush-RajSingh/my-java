package p.composition;

public class Book {
    private int pages;
    private String name;
    private float price;
    public int getPages()
    {
        return pages;
    }
    public void setpages(int pages)
    {
        this.pages = pages;
    }
    public String getname()
    {
        return name;
    }
    public void setname(String name)
    {
        this.name = name;
    }
    public float getPrice()
    {
        return price;
    }
    public void setprice(float price)
    {
        this.price = price;
    }
}
